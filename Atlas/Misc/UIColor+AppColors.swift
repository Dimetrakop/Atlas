import UIKit

extension UIColor {
    class var aBorderColor: UIColor {
        return UIColor(red: 150.0 / 255.0, green: 150.0 / 255.0, blue: 150.0 / 255.0, alpha: 1.0)
    }
    
    class var aTextColorGray: UIColor {
        return UIColor(red: 150.0 / 255.0, green: 150.0 / 255.0, blue: 150.0 / 255.0, alpha: 1.0)
    }
    
    class var aTextBGColorGray: UIColor {
        return UIColor(red: 150.0 / 255.0, green: 150.0 / 255.0, blue: 150.0 / 255.0, alpha: 0.0)
    }

}
